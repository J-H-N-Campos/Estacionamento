    public TelaCadastroVeiculo(Veiculo v)
    {
        novo = false;
        initComponents();

        // popular comboBox
        ArrayList<Pessoa> pessoas = new ArrayList();
        try 
        {
            EstacionamentoPessoaBD bd = BDFactory.newPessoaBD();
            pessoas = bd.readAll();
            
            this.cbPessoa.removeAllItems();
            for (Pessoa pessoa: pessoas)
            {
                this.cbPessoa.addItem(pessoa);
            }
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }
        
        ArrayList<Veiculo> veiculos = new ArrayList();
        try 
        {
            EstacionamentoVeiculoBD bd = BDFactory.newVeiculoBD();
            veiculos = bd.readAll();
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }
        
        VeiculosTableModel vtm = new VeiculosTableModel(veiculos);
        tabelaCadastroVeiculos.setModel(vtm);
        tabelaCadastroVeiculos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        ListSelectionModel selectionModel = tabelaCadastroVeiculos.getSelectionModel();
        selectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e)
            { 
                int row = tabelaCadastroVeiculos.getSelectedRow();
                
                if (row >=0)
                {
                    VeiculosTableModel tableModel = (VeiculosTableModel)tabelaCadastroVeiculos.getModel();
                    veiculo = tableModel.getVeiculos().get(tabelaCadastroVeiculos.getSelectedRow());
                    campoFormatadoCodigo.setInteger(veiculo.getId() );
                    campoFormatadoPlaca.setText(veiculo.getPlaca());
                    campoFormatadoModelo.setText(veiculo.getModelo());
                    cbPessoa.setSelectedItem(veiculo.getPessoa());
                    novo = false;
                }
            }
        } );
    }
    
    public void atualizarTabela()
    {
        ArrayList<Veiculo> veiculos = new ArrayList();
        try 
        {
            EstacionamentoVeiculoBD bd = BDFactory.newVeiculoBD();
            veiculos = bd.readAll();
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }
        
        VeiculosTableModel tableModel = (VeiculosTableModel)tabelaCadastroVeiculos.getModel();
        
        tableModel.setVeiculos(veiculos);
        tabelaCadastroVeiculos.revalidate();
        tabelaCadastroVeiculos.repaint();
    }
    
