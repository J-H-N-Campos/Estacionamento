    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        int id          = campoFormatadoCodigo.getInteger();
        String placa    = campoFormatadoPlaca.getText();
        String modelo   = campoFormatadoModelo.getText();
        Pessoa pessoa   = (Pessoa)cbPessoa.getSelectedItem();
        
        if(!placa.isBlank() && !modelo.isBlank())
        {
            try
            {
                EstacionamentoVeiculoBD bd = BDFactory.newVeiculoBD();
                if (novo)
                {
                    veiculo = new Veiculo(id, placa , modelo, pessoa);
                    bd.create(veiculo);
                    novo = false;
                }
                else
                {
                    veiculo.setId(id);
                    veiculo.setPlaca(placa);
                    veiculo.setModelo(modelo);
                    veiculo.setPessoa(pessoa);
                    bd.edit(veiculo);
                }
            }
            catch (DuplicateKeyException ex)
            {
                System.out.println("Chave duplicada");
            }
            catch (DataBaseException ex)
            {
                System.out.println( ex.getMessage() );
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        }
        
        atualizarTabela();
    }//GEN-LAST:event_botaoSalvarActionPerformed

