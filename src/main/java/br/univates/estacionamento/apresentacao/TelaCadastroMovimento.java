    private void botaoRegistrarEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRegistrarEntradaActionPerformed
        int id               = campoFormatadoCodigo.getInteger();
        String entrada       = campoFormatadoEntrada.getText();
        String saida         = campoFormatadoSaida.getText();
        Veiculo veiculo      = (Veiculo)cbVeiculo.getSelectedItem();
        
        if(!entrada.isBlank() && !saida.isBlank())
        {
            try
            {
                EstacionamentoMovimentoBD bd = BDFactory.newMovimentoBD();
                
                if (novo)
                {
                    movimento = new Movimento(id, entrada, saida, veiculo); 
                    bd.create(movimento);
                    novo = false;
                }
                else
                {
                    movimento.setId(id);
                    movimento.setDtIni(entrada);
                    movimento.setDtFim(saida);
                    movimento.setVeiculo(veiculo);
                    bd.edit(movimento);
                }
            }catch (DuplicateKeyException ex)
            {
                System.out.println("Chave duplicada");
            }
            catch (DataBaseException ex)
            {
                System.out.println( ex.getMessage() );
            }
            atualizarTabela();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos corretamente.");
        }
    }//GEN-LAST:event_botaoRegistrarEntradaActionPerformed

