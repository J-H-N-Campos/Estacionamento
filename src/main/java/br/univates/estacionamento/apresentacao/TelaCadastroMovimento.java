    public TelaCadastroMovimento(Movimento data)
    {
        novo = false;
        this.telaRelatoriosMovimentos = telaRelatoriosMovimentos;
        initComponents();
        
        // popular comboBox
        ArrayList<Veiculo> veiculos = new ArrayList();
        try 
        {
            EstacionamentoVeiculoBD bd = BDFactory.newVeiculoBD();
            veiculos = bd.readAll();
            
            this.cbVeiculo.removeAllItems();
            for (Veiculo veiculo: veiculos)
            {
                this.cbVeiculo.addItem(veiculo);
            }
            
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }

        ArrayList<Movimento> movimentos = new ArrayList();
        try 
        {
            EstacionamentoMovimentoBD bd = BDFactory.newMovimentoBD();
            movimentos = bd.readAll();
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }
        
        MovimentosTableModel tm = new MovimentosTableModel(movimentos);
        tabelaCadastroMovimentos.setModel(tm);
        tabelaCadastroMovimentos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        ListSelectionModel selectionModel = tabelaCadastroMovimentos.getSelectionModel();
        selectionModel.addListSelectionListener( new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e)
            { 
                int row = tabelaCadastroMovimentos.getSelectedRow();
                
                if (row >=0)
                {
                    MovimentosTableModel tableModel = (MovimentosTableModel)tabelaCadastroMovimentos.getModel();
                    movimento = tableModel.getMovimentos().get(tabelaCadastroMovimentos.getSelectedRow());
                    campoFormatadoCodigo.setInteger(movimento.getId() );
                    campoFormatadoEntrada.setText(movimento.getDtIni());
                    campoFormatadoSaida.setText(movimento.getDtFim());
                    cbVeiculo.setSelectedItem(movimento.getVeiculo());

                    novo = false;
                }
            }
        } );        
    }

    public void atualizarTabela()
    {
        ArrayList<Movimento> movimentos = new ArrayList();
        try 
        {
            EstacionamentoMovimentoBD bd = BDFactory.newMovimentoBD();
            movimentos = bd.readAll();
        } 
        catch (DataBaseException ex) 
        {
            System.out.println( ex.getMessage() );
        }
        
        MovimentosTableModel tableModel = (MovimentosTableModel)tabelaCadastroMovimentos.getModel();
        
        tableModel.setMovimentos(movimentos);
        tabelaCadastroMovimentos.revalidate();
        tabelaCadastroMovimentos.repaint();
    }
    
